package ar.edu.iua.project.parcial.exceptions;

public class TareaExisteException extends Exception{
    public TareaExisteException() {
        super();
    }

    public TareaExisteException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public TareaExisteException(String message, Throwable cause) {
        super(message, cause);
    }

    public TareaExisteException(String message) {
        super(message);
    }

    public TareaExisteException(Throwable cause) {
        super(cause);
    }{
    }}