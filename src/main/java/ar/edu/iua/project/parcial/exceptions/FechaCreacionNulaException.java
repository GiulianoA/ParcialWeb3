package ar.edu.iua.project.parcial.exceptions;

public class FechaCreacionNulaException extends Exception{
    public FechaCreacionNulaException() {
        super();
    }

    public FechaCreacionNulaException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public FechaCreacionNulaException(String message, Throwable cause) {
        super(message, cause);
    }

    public FechaCreacionNulaException(String message) {
        super(message);
    }

    public FechaCreacionNulaException(Throwable cause) {
        super(cause);
    }{
    }}
